package Enums;

/**
 * A enumeração OperationType representa os diferentes tipos de operações
 * possíveis.
 * 
 * Tipos disponíveis:
 * - EVACUATION: Representa uma operação de evacuação.
 * - EMERGENCY_COMUNICATION: Representa uma comunicação de emergência.
 * - RESOURCES_DISTRIBUTION: Representa uma operação de distribuição de
 * recursos.
 */
public enum OperationType {
   EVACUATION, EMERGENCY_COMUNICATION, RESOURCES_DISTRIBUTION
}
