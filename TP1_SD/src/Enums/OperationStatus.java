package Enums;

/**
 * A enumeração OperationStatus representa os possíveis estados de uma operação.
 * 
 * Estados disponíveis:
 * - PENDING: A operação está pendente de aprovação.
 * - APPROVED: A operação foi aprovada.
 * - REJECTED: A operação foi rejeitada.
 */
public enum OperationStatus {
    PENDING,
    APPROVED,
    REJECTED
}
