package Enums;

/**
 * A enumeração Rank representa os diferentes níveis de hierarquia ou permissão
 * atribuídos a um utilizador.
 * 
 * Níveis disponíveis:
 * - NONE: Sem nível atribuído.
 * - LOW: Nível baixo, acesso limitado.
 * - MEDIUM: Nível médio, acesso moderado.
 * - HIGH: Nível alto, acesso completo.
 */
public enum Rank {
    NONE, LOW, MEDIUM, HIGH
}
